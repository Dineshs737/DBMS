SELECT DISTINCT
    SA.student_id, 
    S.name AS StudentName,
    (CASE 
        WHEN SA.attendance_percentage >= 80.00 THEN 'Eligible'
        ELSE 'Not Eligible'
    END) AS eligibility
FROM 
    attendance AS SA
JOIN 
    student AS S ON SA.student_id = S.student_id;

